"""Probatio: put your data to the proof.

Probatio validates Python data structures against a schema. It is a modern,
maintained data validation library that is API-compatible with voluptuous, so
it can be dropped in where voluptuous is used, while being free to improve the
internals, error model, and tooling underneath.

This module is the public facade: it re-exports the schema engine, markers,
validators, the error hierarchy, the JSON Schema / OpenAPI / field-list codecs,
and the JSON/YAML/TOML loaders and dumpers.
"""

